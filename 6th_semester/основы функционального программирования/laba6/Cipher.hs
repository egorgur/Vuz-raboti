module Cipher
-- `module Cipher ( ... ) where` — объявление модуля со СПИСКОМ ЭКСПОРТА.
--   Зачем список экспорта: наружу видны только перечисленные имена, а
--   вспомогательные функции (shiftChar и т.п.) остаются приватными — это и есть
--   инкапсуляция «чистой логики».
  ( CipherMode(..)        -- экспортируем тип и ВСЕ его конструкторы (`(..)`)
  , vigenereEncrypt       -- шифрование Виженера
  , vigenereDecrypt       -- дешифрование Виженера
  , substitutionEncrypt   -- шифр подстановки по таблице
  , substitutionDecrypt   -- дешифрование подстановки
  , invertTable           -- обращение таблицы подстановки
  , makeTable             -- безопасное построение таблицы из двух алфавитов
  , validateKey           -- валидация ключа
  ) where

import Data.Char (isAsciiUpper, isAsciiLower, ord, chr)
-- Выборочный импорт функций над символами.
--   `isAsciiUpper`/`isAsciiLower` — проверки латинских заглавных/строчных букв;
--   `ord :: Char -> Int` и `chr :: Int -> Char` — код символа и обратно.
--   Зачем именно ASCII-проверки: шифр работает с латиницей A–Z/a–z; так мы
--   не трогаем небуквенные символы и не ломаемся на не-ASCII буквах.

import Data.Maybe (fromMaybe)
-- `fromMaybe :: a -> Maybe a -> a` — берёт значение из Just или значение
--   по умолчанию из Nothing. Зачем: при подстановке символ без записи в
--   таблице остаётся самим собой (значение по умолчанию).

-- -----------------------------------------------------------------------------
-- Тип режима шифрования (АТД-«перечисление»)
-- -----------------------------------------------------------------------------

data CipherMode = Encrypt | Decrypt
-- `data CipherMode = Encrypt | Decrypt` — сумма типов (перечисление) с двумя
--   конструкторами без полей.
--   Зачем АТД, а не строка/булево: типобезопасность — компилятор гарантирует,
--   что режим может быть только Encrypt или Decrypt, опечатка невозможна.
  deriving (Eq, Show)
-- `deriving (Eq, Show)` — автоматически создаём экземпляры сравнения и вывода.
--   Зачем: чтобы сравнивать режим (`mode == Encrypt`) и печатать его при отладке.

-- -----------------------------------------------------------------------------
-- Вспомогательные ЧИСТЫЕ функции (не экспортируются — приватные)
-- -----------------------------------------------------------------------------

isAsciiLetter :: Char -> Bool
-- Сигнатура предиката над символом.

isAsciiLetter c = isAsciiUpper c || isAsciiLower c
-- `||` — логическое «ИЛИ». Буква латиницы — это заглавная ИЛИ строчная.
--   Зачем: единый критерий «шифруемого» символа, используется и в ключе, и в тексте.

keyShift :: Char -> Int
-- По букве ключа возвращаем сдвиг 0..25 (A/a → 0, B/b → 1, ...).

keyShift c
  | isAsciiUpper c = ord c - ord 'A'
-- Guard: для заглавной буквы сдвиг = расстояние от 'A' по кодам.
  | isAsciiLower c = ord c - ord 'a'
-- Для строчной — расстояние от 'a'.
  | otherwise      = 0
-- Иначе 0 (не должно встретиться: ключ предварительно валидируется).
--   Зачем разделять регистр: коды 'A' и 'a' различны, нужна своя база отсчёта.

shiftChar :: Int -> Char -> Char
-- Сдвиг буквы на `n` позиций по кругу алфавита, с сохранением регистра.

shiftChar n c
  | isAsciiUpper c = chr ((ord c - ord 'A' + n) `mod` 26 + ord 'A')
-- Для заглавной: переводим в 0..25, прибавляем сдвиг, берём `mod 26`
--   (кольцо алфавита), возвращаемся к коду буквы через `+ ord 'A'`.
--   Обратные кавычки `` `mod` `` делают функцию `mod` инфиксной.
--   `mod` в Haskell даёт неотрицательный результат, поэтому отрицательный сдвиг
--   (при дешифровании) корректно «заворачивается» в диапазон 0..25.
  | isAsciiLower c = chr ((ord c - ord 'a' + n) `mod` 26 + ord 'a')
-- То же для строчных, с базой 'a' — так сохраняется регистр.
  | otherwise      = c
-- Небуквенные символы (пробелы, цифры, знаки) проходят без изменений.
--   Зачем `mod 26`: алфавит циклический; 'Z'+1 должно стать 'A'.

-- -----------------------------------------------------------------------------
-- Виженер: общая реализация, параметризованная знаком сдвига
-- -----------------------------------------------------------------------------

vigenereCipher :: Int -> String -> String -> String
-- `sign` (+1 шифрование / -1 дешифрование), ключ, текст -> результат.
--   Каррирование: функция трёх аргументов как цепочка одноаргументных.

vigenereCipher sign key text
  | null shifts = text
-- Guard: если в ключе нет букв (пустой/невалидный) — возвращаем текст как есть.
--   Зачем: защита от `cycle []`, которое зациклилось бы; основную проверку
--   ключа делает validateKey, но чистая функция остаётся тотальной.
  | otherwise   = go (cycle shifts) text
-- `cycle shifts` — БЕСКОНЕЧНЫЙ циклический повтор сдвигов ключа (ленивость!).
--   Зачем cycle: ключ короче текста и должен повторяться; ленивый бесконечный
--   список берётся ровно настолько, насколько нужно по длине текста.
  where
    shifts = map keyShift (filter isAsciiLetter key)
-- `filter isAsciiLetter key` оставляет в ключе только буквы,
--   `map keyShift` превращает их в числовые сдвиги.
--   Композиция map/filter — классический функциональный приём обработки списка.
    go _ [] = []
-- Базовый случай рекурсии: текст кончился — результат пуст.
    go (k:rest) (c:cs)
-- Сопоставление с образцом по голове/хвосту обоих списков: `k` — текущий сдвиг
--   ключа, `c` — текущий символ текста.
      | isAsciiLetter c = shiftChar (sign * k) c : go rest cs
-- Буква: сдвигаем на `sign * k` (знак задаёт направление) и берём СЛЕДУЮЩИЙ
--   сдвиг ключа (`rest`). `:` строит результат лениво.
      | otherwise       = c : go (k:rest) cs
-- Не-буква: оставляем символ и НЕ продвигаем ключ (`k:rest` тот же).
--   Зачем не продвигать: классический Виженер пропускает пробелы/знаки,
--   синхронизируя ключ только с буквами.
    go [] cs = cs
-- Недостижимо при непустом ключе (cycle бесконечен), но делает функцию
--   тотальной и убирает предупреждение о неполноте образцов.

-- -----------------------------------------------------------------------------
-- Публичные функции Виженера
-- -----------------------------------------------------------------------------

vigenereEncrypt :: String -> String -> String
-- Сигнатура: ключ -> текст -> шифртекст (ключ первым аргументом).

vigenereEncrypt = vigenereCipher 1
-- Частичное применение (point-free): фиксируем знак +1, остальные аргументы
--   «дотекут» сами. Зачем: лаконично выразить шифрование через общую функцию.

vigenereDecrypt :: String -> String -> String
vigenereDecrypt = vigenereCipher (-1)
-- То же со знаком -1 — обратный сдвиг. Скобки вокруг `(-1)` обязательны,
--   иначе это воспринялось бы как вычитание.

-- -----------------------------------------------------------------------------
-- Шифр подстановки
-- -----------------------------------------------------------------------------

substitutionEncrypt :: [(Char, Char)] -> String -> String
-- Таблица замен (ассоциативный список пар) и текст -> результат.

substitutionEncrypt table = map (\c -> fromMaybe c (lookup c table))
-- `map (\c -> ...)` применяет лямбду к каждому символу.
--   `lookup c table :: Maybe Char` ищет замену для `c`; `fromMaybe c` берёт
--   найденную замену либо оставляет `c`, если замены нет.
--   Лямбда `\c -> ...` — анонимная функция; `\` вводит параметр.
--   Зачем map + lookup + fromMaybe: декларативно «заменить каждый символ по
--   таблице, неизвестные — без изменений».

invertTable :: [(Char, Char)] -> [(Char, Char)]
-- Обращение таблицы: меняем местами «откуда» и «куда».

invertTable = map (\(a, b) -> (b, a))
-- Лямбда с образцом-кортежем `\(a, b) -> (b, a)` распаковывает пару и
--   переставляет элементы. Зачем: дешифрование подстановки = шифрование по
--   обратной таблице.

substitutionDecrypt :: [(Char, Char)] -> String -> String
substitutionDecrypt table = substitutionEncrypt (invertTable table)
-- Дешифрование = подстановка по обращённой таблице. Переиспользуем уже
--   написанную логику — типичный приём «выразить одно через другое».

-- -----------------------------------------------------------------------------
-- Построение таблицы и валидация (граничные проверки чистой логики)
-- -----------------------------------------------------------------------------

makeTable :: String -> String -> Either String [(Char, Char)]
-- Из двух алфавитов («откуда» и «куда») строим таблицу или сообщаем об ошибке.
--   `Either String [(Char,Char)]` — либо ошибка-строка, либо готовая таблица.

makeTable from to
  | length from /= length to =
      Left "Substitution alphabets must have equal length"
-- Guard: при несовпадении длин (`/=` — «не равно») возвращаем Left с причиной.
--   Зачем: обработка граничного случая «несовпадение длин при подстановке».
  | otherwise = Right (zip from to)
-- `zip from to` попарно соединяет символы в список пар — это и есть таблица.
--   Заворачиваем в Right как успешный результат.

validateKey :: String -> Either String ()
-- Валидация ключа Виженера. Результат — `Either String ()`: при успехе
--   полезного значения нет (`()`), важен лишь факт «ок/ошибка».

validateKey key
  | null key            = Left "Key must not be empty"
-- `null` проверяет пустоту списка/строки.
  | all isAsciiLetter key = Right ()
-- `all p xs` — истинно, если предикат `p` выполнен для ВСЕХ элементов.
--   Здесь: все символы ключа — латинские буквы.
  | otherwise           = Left "Key must contain only letters"
-- Иначе сообщаем, что ключ содержит недопустимые символы.
--   Зачем Either, а не Maybe: нужно вернуть ТЕКСТ причины ошибки, а не просто
--   признак неудачи — это требование «детализированных ошибок» бизнес-логики.
