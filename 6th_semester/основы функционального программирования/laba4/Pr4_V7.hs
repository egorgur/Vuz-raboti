-- =============================================================================
-- Pr4_V7.hs — Практическая работа №4. Вариант 7: Игра «Угадай число»
-- =============================================================================
-- Компьютер загадывает число от 1 до 100, игрок угадывает, получая подсказки
-- «больше»/«меньше»/«угадал». Считается число попыток; после партии
-- предлагается сыграть снова. Обрабатываются: некорректный ввод, выход числа
-- за диапазон и конец ввода (EOF, Ctrl+D).
--
-- Ниже каждая значимая строка снабжена комментарием: какой синтаксис Haskell
-- применён и зачем именно он понадобился.
-- =============================================================================

module Main where
-- `module Main where` — объявление модуля с именем Main.
--   Зачем: модуль Main, содержащий `main`, — это исполняемая программа
--   (точка входа). Ключевое слово `where` открывает список определений.

import System.Random (randomRIO)
-- Импорт одной функции из пакета `random`.
--   `randomRIO :: (a, a) -> IO a` — выдаёт случайное число в заданном диапазоне.
--   Зачем: по условию компьютер «загадывает» число через randomRIO (1, 100).
--   Действие живёт в IO, потому что генерация случайности — побочный эффект.

import System.IO (hSetBuffering, BufferMode(NoBuffering), stdout, isEOF)
-- Выборочный импорт средств ввода-вывода.
--   `hSetBuffering` — настройка буферизации потока; `BufferMode(NoBuffering)` —
--   конструктор режима «без буфера»; `stdout` — стандартный вывод;
--   `isEOF :: IO Bool` — проверка, достигнут ли конец ввода.
--   Запись `BufferMode(NoBuffering)` импортирует тип BufferMode вместе с нужным
--   конструктором. Зачем: чтобы приглашения печатались сразу (без буфера) и
--   чтобы корректно ловить EOF (Ctrl+D), а не падать с исключением.

import Text.Read (readMaybe)
-- Импорт безопасного парсера.
--   `readMaybe :: Read a => String -> Maybe a` — пытается разобрать строку,
--   возвращает Nothing вместо аварии при неудаче.
--   Зачем: по требованиям нельзя использовать падающие `read`/`fromJust`;
--   readMaybe даёт безопасный разбор числа.

-- -----------------------------------------------------------------------------
-- readGuess — безопасное чтение числа с проверкой диапазона 1..100
-- -----------------------------------------------------------------------------

readGuess :: IO (Either String Int)
-- Сигнатура: действие IO, возвращающее `Either String Int`.
--   `Either String Int` — либо `Left <сообщение об ошибке>`, либо `Right <число>`.
--   Зачем именно Either (а не Maybe): нужно не просто «ошибка/нет», а текст
--   причины (что именно не так с вводом), поэтому в Left кладём String.

readGuess = do
-- `do`-нотация для последовательности IO-действий.
--   Зачем: чтение строки — побочный эффект; do связывает шаги по порядку.
  line <- getLine
-- `line <- getLine` — оператор связывания `<-` извлекает результат IO-действия.
--   `getLine :: IO String` читает строку с консоли; `<-` «достаёт» String в `line`.
--   Зачем `<-`, а не `let`: getLine — это эффект в IO, его результат
--   извлекается именно стрелкой связывания.
  case readMaybe line :: Maybe Int of
-- `case <выражение> of` — разбор результата по образцам.
--   `readMaybe line :: Maybe Int` — аннотация типа `:: Maybe Int` подсказывает,
--   что парсить нужно именно в Int (иначе тип неоднозначен).
--   Зачем case: по результату (Nothing/Just) выбираем разные ветви.
    Nothing ->
-- Образец `Nothing` — разбор не удался (введено не число).
      return (Left ("это не целое число: " ++ show line))
-- `return` упаковывает чистое значение в монаду IO (`a -> IO a`).
--   `Left (...)` — конструируем ошибку. `++` склеивает строки,
--   `show line` берёт строку в кавычки для наглядности.
--   Зачем `return`: тип функции — IO (...), поэтому готовый Either нужно
--   «поднять» в IO; сам по себе он не является IO-действием.
    Just n
-- Образец `Just n` — строка успешно разобрана в число `n`.
      | n < 1 || n > 100 ->
-- Охранное выражение (guard): `|` вводит условие, `||` — логическое «ИЛИ».
--   Срабатывает, если число вышло за диапазон 1..100.
--   Зачем guard: компактно проверить условие прямо в ветке образца.
          return (Left ("число вне диапазона 1..100: " ++ show n))
-- Возвращаем ошибку диапазона. `show n` превращает Int в строку.
      | otherwise ->
-- `otherwise` — это просто `True`; «иначе» (число корректно и в диапазоне).
          return (Right n)
-- Упаковываем успешный результат в `Right` и поднимаем в IO через `return`.

-- -----------------------------------------------------------------------------
-- playRound — один раунд: спрашиваем число, пока не угадано или не EOF
-- -----------------------------------------------------------------------------

playRound :: Int -> Int -> IO ()
-- Сигнатура: загаданное число, счётчик попыток, результат — IO () (только эффект).
--   `IO ()` — действие без полезного результата (`()` — «ничего»).
--   Каррирование: `Int -> Int -> IO ()` принимает аргументы по одному.

playRound secret attempts = do
-- Два параметра: `secret` (загадка) и `attempts` (сделано попыток).
  putStr "Ваш вариант (1..100): "
-- `putStr` печатает строку без перевода строки (приглашение к вводу).
--   Зачем putStr (не putStrLn): курсор остаётся на той же строке после «: ».
  eof <- isEOF
-- Извлекаем результат `isEOF :: IO Bool` — достигнут ли конец ввода.
--   Зачем: проверяем EOF ДО чтения, чтобы getLine не выбросил исключение.
  if eof
-- `if <условие> then <ветка1> else <ветка2>` — условное выражение.
--   В Haskell `if` — выражение и обязан иметь обе ветви одного типа (IO ()).
    then putStrLn "\nВвод завершён (EOF). До встречи!"
-- При EOF печатаем сообщение (`\n` — перевод строки) и корректно выходим
--   из раунда. Зачем: требование — аккуратное завершение по Ctrl+D.
    else do
-- Иначе продолжаем читать и обрабатывать ввод (вложенный do-блок).
      result <- readGuess
-- Вызываем нашу безопасную функцию; `result :: Either String Int`.
      case result of
-- Разбираем: ошибка (Left) или корректное число (Right).
        Left err -> do
-- Ветка ошибки: `err` — текст причины.
          putStrLn ("Ошибка: " ++ err)
-- Печатаем сообщение об ошибке с переводом строки.
          playRound secret attempts
-- Рекурсивный вызов того же раунда — повтор запроса.
--   Зачем рекурсия (а не цикл): в Haskell повторение реализуется рекурсией;
--   счётчик `attempts` НЕ увеличиваем, т.к. некорректный ввод — не попытка.
        Right guess ->
-- Ветка успеха: `guess` — корректное число игрока.
          let attempts' = attempts + 1
-- `let <имя> = <выражение> in <тело>` — локальное связывание в выражении.
--   `attempts'` (со штрихом — допустимый символ в имени) = увеличенный счётчик.
--   Зачем: засчитываем только валидную попытку.
          in case compare guess secret of
-- `compare :: Ord a => a -> a -> Ordering` возвращает LT, EQ или GT.
--   Зачем compare: один вызов даёт все три исхода сравнения сразу.
               LT -> do
-- `LT` — guess меньше secret, значит загаданное число больше.
                 putStrLn "Загаданное число больше."
                 playRound secret attempts'
-- Подсказка и повтор раунда с увеличенным счётчиком.
               GT -> do
-- `GT` — guess больше secret, значит загаданное число меньше.
                 putStrLn "Загаданное число меньше."
                 playRound secret attempts'
               EQ ->
-- `EQ` — числа равны: игрок угадал.
                 putStrLn ("Поздравляю! Вы угадали за " ++ show attempts' ++ " попыток.")
-- Печатаем результат с числом попыток; раунд завершается (рекурсии нет).

-- -----------------------------------------------------------------------------
-- gameLoop — одна партия + предложение сыграть снова
-- -----------------------------------------------------------------------------

gameLoop :: IO ()
-- Действие без результата: проводит партию и спрашивает про повтор.

gameLoop = do
  secret <- randomRIO (1, 100) :: IO Int
-- `randomRIO (1, 100)` генерирует случайное число; `:: IO Int` фиксирует тип,
--   чтобы границы 1 и 100 трактовались как Int.
--   `<-` извлекает сгенерированное число в `secret`.
--   Зачем аннотация: без неё тип числа неоднозначен (Int? Integer?).
  putStrLn "Я загадал число от 1 до 100. Угадывайте!"
-- Печать с переводом строки — вводное сообщение партии.
  playRound secret 0
-- Запуск раунда: передаём загадку и стартовый счётчик попыток 0.
  putStr "Сыграть ещё раз? (y/n): "
-- Приглашение к ответу без перевода строки.
  eof <- isEOF
-- Снова проверяем EOF перед чтением ответа.
  if eof
    then putStrLn "\nЗавершение игры."
-- При EOF завершаем без чтения (защита от исключения getLine).
    else do
      answer <- getLine
-- Читаем ответ пользователя в `answer :: String`.
      if answer `elem` ["y", "Y", "д", "Д", "yes", "да"]
-- `elem` проверяет вхождение в список; обратные кавычки делают его инфиксным:
--   `answer `elem` [...]` читается как «answer входит в список».
--   Зачем список вариантов: принимаем разные формы согласия.
        then gameLoop
-- Согласие — рекурсивно запускаем новую партию (новое случайное число).
        else putStrLn "Спасибо за игру!"
-- Любой другой ответ — выходим с прощанием.

-- -----------------------------------------------------------------------------
-- main — точка входа
-- -----------------------------------------------------------------------------

main :: IO ()
-- Обязательная сигнатура точки входа: действие ввода-вывода без результата.

main = do
  hSetBuffering stdout NoBuffering
-- Отключаем буферизацию stdout, чтобы приглашения (`putStr` без `\n`)
--   появлялись на экране сразу, до ввода пользователя.
--   Зачем: иначе текст «...: » мог бы зависнуть в буфере и не показаться вовремя.
  putStrLn "=== Игра «Угадай число» ==="
-- Заголовок программы.
  gameLoop
-- Запускаем игровой цикл; вся дальнейшая логика — внутри него.
