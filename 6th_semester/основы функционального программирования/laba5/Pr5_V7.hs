-- =============================================================================
-- Pr5_V7.hs — Практическая работа №5. Вариант 7: Ленивая матрица Паскаля
-- =============================================================================
-- Бесконечный треугольник Паскаля, доступный по координатам. Благодаря
-- ленивости вся структура задаётся самореференцией и вычисляется только
-- настолько, насколько к ней обращаются. Уже вычисленные строки кэшируются
-- (мемоизация «из коробки»), поэтому повторные обращения не пересчитываются.
--
-- Каждая значимая строка снабжена комментарием: какой синтаксис Haskell
-- применён и зачем именно он понадобился.
-- =============================================================================

module Main where
-- `module Main where` — объявление модуля Main (исполняемая программа).
--   `where` открывает блок определений модуля. Наличие `main` делает файл
--   запускаемым.

-- -----------------------------------------------------------------------------
-- pascal — бесконечный треугольник Паскаля как список строк
-- -----------------------------------------------------------------------------

pascal :: [[Integer]]
-- Сигнатура: `[[Integer]]` — список списков целых (`Integer` — длинная
--   арифметика без переполнения). Строка n имеет длину n+1.
--   Зачем Integer (а не Int): биномиальные коэффициенты быстро становятся
--   огромными (например C(100,50)), Int их не вмещает.

pascal = [1] : map nextRow pascal
-- Самореферентное (рекурсивное) определение бесконечной структуры.
--   `:` — оператор «cons», добавляет элемент в голову списка: голова — первая
--   строка `[1]` (нулевая строка треугольника).
--   `map nextRow pascal` — применяем `nextRow` к КАЖДОЙ строке самого `pascal`,
--   получая все последующие строки. То есть строка (n+1) строится из строки n.
--   Зачем самореференция + ленивость: `pascal` упоминает сам себя, и это
--   работает ТОЛЬКО благодаря отложенным вычислениям — голова `[1]` известна
--   сразу, а `map nextRow pascal` производит элементы по запросу, опираясь на
--   уже готовую часть списка. В энергичном языке такая запись зациклилась бы.
  where
-- `where` вводит локальное определение, видимое в теле `pascal`.
    nextRow :: [Integer] -> [Integer]
-- Локальная сигнатура вспомогательной функции «следующая строка».
--   Зачем явная сигнатура: документирует, что из одной строки получаем другую.
    nextRow row = zipWith (+) (0 : row) (row ++ [0])
-- Построение следующей строки из текущей `row`.
--   `0 : row` — приписываем 0 слева (сдвиг вправо), `row ++ [0]` — 0 справа.
--   `zipWith (+) xs ys` — поэлементно складывает два списка.
--   Зачем именно так: каждый элемент новой строки — сумма двух соседних сверху;
--   нули по краям дают единицы на концах. Пример: из [1,2,1] получаем
--   zipWith (+) [0,1,2,1] [1,2,1,0] = [1,3,3,1].

-- -----------------------------------------------------------------------------
-- pascalRow — n-я строка треугольника (нумерация с 0)
-- -----------------------------------------------------------------------------

pascalRow :: Int -> [Integer]
-- Сигнатура: по номеру строки возвращаем сам список коэффициентов.

pascalRow n = pascal !! n
-- `!!` — оператор индексации списка (0-based): берёт n-й элемент.
--   Здесь n-й элемент `pascal` — это и есть n-я строка треугольника.
--   Зачем: благодаря ленивости вычислятся только строки с 0 по n, остальные
--   останутся невычисленными; повторный вызов использует уже готовые строки.
--   (Отрицательный n не определён: `!!` для него аварийно завершится.)

-- -----------------------------------------------------------------------------
-- pascalAt — элемент (строка, столбец) = биномиальный коэффициент C(n,k)
-- -----------------------------------------------------------------------------

pascalAt :: Int -> Int -> Integer
-- Сигнатура двух аргументов (строка, столбец) -> значение.
--   Каррирование: `Int -> Int -> Integer` = `Int -> (Int -> Integer)`.

pascalAt n k
-- Определение с охранными выражениями (guards) по аргументам n и k.
  | k < 0 || k > n = 0
-- Guard: `|` вводит условие, `||` — логическое «ИЛИ».
--   Если столбец вне строки (k<0 или k>n), коэффициент равен 0.
--   Зачем: по условию `pascalAt n k при k > n → 0`; так мы избегаем выхода
--   за границы строки (в строке n всего n+1 элементов с индексами 0..n).
  | otherwise      = pascalRow n !! k
-- `otherwise` ≡ True — «иначе»: индекс корректен, берём k-й элемент n-й строки.
--   Зачем: переиспользуем `pascalRow`, опираясь на ту же ленивую структуру.

-- -----------------------------------------------------------------------------
-- binomial — биномиальный коэффициент C(n,k) через pascalAt
-- -----------------------------------------------------------------------------

binomial :: Integer -> Integer -> Integer
-- Сигнатура с `Integer`, чтобы принимать сколь угодно большие n, k.

binomial n k = pascalAt (fromInteger n) (fromInteger k)
-- `fromInteger :: Integer -> Int` приводит Integer к Int для индексации.
--   Зачем приведение: `pascalAt` индексирует список и работает с `Int`, а
--   условие требует выразить binomial «через pascalAt». Граничные случаи
--   (k<0 или k>n → 0) уже обработаны внутри pascalAt.

-- -----------------------------------------------------------------------------
-- main — демонстрация и проверка
-- -----------------------------------------------------------------------------

main :: IO ()
-- Точка входа: действие ввода-вывода без полезного результата (`()`).

main = do
-- `do`-нотация — последовательность IO-действий (печать).
  putStrLn "Первые 6 строк треугольника Паскаля:"
-- `putStrLn` печатает строку с переводом строки.
  mapM_ print (take 6 pascal)
-- `take 6 pascal` лениво берёт ровно 6 первых строк бесконечного списка.
--   `mapM_ print` применяет `print` к каждой строке (печать + перевод строки),
--   отбрасывая результат (суффикс `_`).
--   Зачем take: без него попытка напечатать весь бесконечный `pascal` не
--   завершилась бы; take обрезает поток до конечного числа строк.
  putStrLn ("pascalRow 5    = " ++ show (pascalRow 5))
-- Демонстрация строки №5: [1,5,10,10,5,1]. `++` склеивает строки,
--   `show` превращает список в строку.
  putStrLn ("pascalAt 5 2   = " ++ show (pascalAt 5 2))
-- Элемент C(5,2) = 10.
  putStrLn ("pascalAt 4 5   = " ++ show (pascalAt 4 5))
-- Граничный случай k>n: ожидаем 0.
  putStrLn ("binomial 10 3  = " ++ show (binomial 10 3))
-- C(10,3) = 120.
  putStrLn ("binomial 52 5  = " ++ show (binomial 52 5))
-- C(52,5) = 2598960 (число раздач 5 карт из колоды) — демонстрация Integer.
  putStrLn ("pascalAt 100 50 = " ++ show (pascalAt 100 50))
-- Большой коэффициент: показывает мемоизацию и пользу длинной арифметики.
