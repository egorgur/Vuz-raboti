module Main where
-- `module Main where` — объявление модуля.
--   Синтаксис: ключевое слово `module`, имя модуля (`Main`), ключевое слово `where`,
--   после которого идут определения модуля.
--   Зачем: модуль с именем Main и функцией main — это точка входа исполняемой
--   программы. Без него файл воспринимается просто как библиотека.

import Prelude hiding (Rational)
-- `import Prelude hiding (Rational)` — импорт стандартной библиотеки Prelude,
--   но с сокрытием (`hiding`) одного имени — `Rational`.
--   Синтаксис: `import <Модуль> hiding (<список скрываемых имён>)`.
--   Зачем: в Prelude уже есть имя `Rational` (синоним `type Rational = Ratio Integer`).
--   Если его не скрыть, наше собственное определение `data Rational ...` вызовет
--   конфликт имён. `hiding` убирает чужое имя, освобождая его для нашего типа.

import Data.Ratio (numerator, denominator)
-- `import Data.Ratio (numerator, denominator)` — импорт ровно двух функций из модуля.
--   Синтаксис: `import <Модуль> (<явный список имён>)` — выборочный импорт.
--   Зачем: понадобятся при реализации `fromRational` в классе Fractional,
--   чтобы разложить стандартную дробь на числитель и знаменатель.
--   Выборочный импорт экономнее, чем тянуть весь модуль целиком.

-- -----------------------------------------------------------------------------
-- Определение типа
-- -----------------------------------------------------------------------------

data Rational = R Int Int
-- `data Rational = R Int Int` — объявление нового алгебраического типа данных.
--   Синтаксис: `data <ИмяТипа> = <Конструктор> <ТипПоля1> <ТипПоля2> ...`.
--   `Rational` — имя типа (с заглавной буквы, как все типы).
--   `R` — конструктор данных (тоже с заглавной), который упаковывает два `Int`:
--   первый — числитель, второй — знаменатель.
--   Зачем: это «произведение типов» (пара чисел). Конструктор `R` позволяет
--   создавать значения (`R 3 4`) и разбирать их через сопоставление с образцом.

-- -----------------------------------------------------------------------------
-- reduce — сокращение дроби на НОД и нормализация знака (знаменатель > 0)
-- -----------------------------------------------------------------------------

reduce :: Rational -> Rational
-- Сигнатура типа функции.
--   Синтаксис: `<имя> :: <типАргумента> -> <типРезультата>`.
--   `::` читается как «имеет тип». Стрелка `->` обозначает функцию.
--   Зачем: явная сигнатура документирует намерение и помогает компилятору
--   ловить ошибки; здесь функция берёт Rational и возвращает Rational.

reduce (R n d) = R (n * s `div` g) (d * s `div` g)
-- Тело функции с сопоставлением с образцом `(R n d)`.
--   Синтаксис: слева от `=` — образец, который «распаковывает» конструктор `R`,
--   связывая `n` с числителем и `d` со знаменателем.
--   `n * s `div` g` — арифметика: `*` и `div` имеют одинаковый приоритет и
--   левую ассоциативность, поэтому это читается как `(n * s) `div` g`.
--   Обратные кавычки `` `div` `` превращают обычную функцию `div` в инфиксный
--   оператор (вместо `div (n*s) g` пишем `(n*s) `div` g` — нагляднее).
--   Зачем: одновременно делим числитель и знаменатель на НОД (`g`) и домножаем
--   на знак `s`, чтобы знаменатель стал положительным.
  where s = signum d
-- `where` вводит локальные определения, видимые в теле функции выше.
--   `signum d` возвращает -1, 0 или 1 в зависимости от знака `d`.
--   Зачем `s`: умножение числителя и знаменателя на знак знаменателя
--   переносит минус «наверх» и делает знаменатель неотрицательным.
        g = gcd n d
-- Второе локальное определение в том же блоке `where`.
--   `gcd n d` — стандартная функция наибольшего общего делителя (всегда >= 0).
--   Зачем `g`: деление обоих компонентов на НОД даёт несократимую дробь.

-- -----------------------------------------------------------------------------
-- add — сложение: a/b + c/d = (a*d + c*b) / (b*d)
-- -----------------------------------------------------------------------------

add :: Rational -> Rational -> Rational
-- Сигнатура функции двух аргументов.
--   Синтаксис: `тип1 -> тип2 -> результат`. Стрелки правоассоциативны, то есть
--   это на самом деле `Rational -> (Rational -> Rational)` — каррирование:
--   функция принимает один аргумент и возвращает функцию на второй.
--   Зачем: так в Haskell любая «функция нескольких аргументов» естественно
--   поддерживает частичное применение.

add (R a b) (R c d) = reduce (R (a*d + c*b) (b*d))
-- Сразу два образца `(R a b)` и `(R c d)` — распаковываем обе дроби.
--   `a*d + c*b` — числитель суммы по формуле приведения к общему знаменателю.
--   `b*d` — общий знаменатель. Результат оборачиваем в `R` и сразу сокращаем
--   через `reduce`.
--   Зачем `reduce`: чтобы результат сложения всегда оставался в нормальной
--   (несократимой) форме с положительным знаменателем.

-- -----------------------------------------------------------------------------
-- multiply — умножение: a/b * c/d = (a*c) / (b*d)
-- -----------------------------------------------------------------------------

multiply :: Rational -> Rational -> Rational
-- Сигнатура: две дроби на входе, одна на выходе (см. пояснение к `add`).

multiply (R a b) (R c d) = reduce (R (a*c) (b*d))
-- Перемножаем числители (`a*c`) и знаменатели (`b*d`) и сокращаем результат.
--   Зачем `reduce`: умножение часто порождает большие сократимые дроби
--   (например 2/4 * 3/9), нормализация сохраняет канонический вид.

-- -----------------------------------------------------------------------------
-- toDouble — преобразование рационального числа в Double
-- -----------------------------------------------------------------------------

toDouble :: Rational -> Double
-- Сигнатура: на входе Rational, на выходе число с плавающей точкой `Double`.

toDouble (R a b) = fromIntegral a / fromIntegral b
-- `fromIntegral` преобразует целое (`Int`) в любой числовой тип (здесь — `Double`).
--   Синтаксис: обычное применение функции `fromIntegral a`.
--   `/` — оператор деления для дробных типов (`Fractional`), даёт `Double`.
--   Зачем `fromIntegral`: оператор `/` не работает с `Int`; нужно сначала
--   расширить целые до `Double`, иначе тип не сойдётся.

-- -----------------------------------------------------------------------------
-- instance Num Rational — арифметика через add, multiply, negate
-- -----------------------------------------------------------------------------

instance Num Rational where
-- `instance Num Rational where` — объявление экземпляра класса типов `Num`
--   для нашего типа `Rational`.
--   Синтаксис: `instance <Класс> <Тип> where <реализации методов>`.
--   Зачем: класс `Num` даёт стандартные операторы (+), (*), negate и т.д.
--   Реализовав его, мы сможем писать `z1 + z2` вместо `add z1 z2` и
--   использовать числовые литералы как Rational.
  (+) = add
-- Определяем оператор `(+)` как нашу функцию `add`.
--   Синтаксис: `(+)` в скобках — это оператор, взятый как обычная функция.
--   Запись «бесточечная» (point-free): просто приравниваем один к другому.
--   Зачем: логика сложения уже написана в `add`, переиспользуем её.
  (*) = multiply
-- Аналогично связываем оператор умножения `(*)` с функцией `multiply`.
  negate (R a b) = R (negate a) b
-- Метод `negate` (унарный минус). Меняем знак только числителя.
--   `negate a` — вложенный вызов negate для `Int`.
--   Зачем: -(a/b) = (-a)/b; знаменатель трогать не нужно.
  abs (R a b) = R (abs a) (abs b)
-- Метод `abs` (модуль). Берём модуль числителя и знаменателя по отдельности.
--   Зачем: |a/b| = |a|/|b|; обе части делаем неотрицательными.
  signum (R a b) = R (signum a * signum b) 1
-- Метод `signum` (знак числа): -1, 0 или 1, представленные как дробь n/1.
--   `signum a * signum b` — знак дроби равен произведению знаков частей.
--   Зачем `1` в знаменателе: результат signum обязан быть того же типа
--   (Rational), поэтому знак записываем как целую дробь (-1/1, 0/1, 1/1).
  fromInteger n = R (fromInteger n) 1
-- Метод `fromInteger` превращает целочисленный литерал в Rational.
--   `fromInteger n` справа — преобразование `Integer` в `Int` для поля.
--   Зачем: благодаря этому методу можно писать просто `5 :: Rational`,
--   и оно станет `R 5 1`. Любое целое n = n/1.

-- -----------------------------------------------------------------------------
-- instance Fractional Rational — деление: a/b / c/d = (a*d) / (b*c)
-- -----------------------------------------------------------------------------

instance Fractional Rational where
-- Экземпляр класса `Fractional` — класс типов, поддерживающих деление `/`.
--   Зачем: позволяет применять оператор `(/)` к нашим дробям.
  (R a b) / (R c d) = reduce (R (a*d) (b*c))
-- Определение оператора `(/)` в инфиксной форме сразу с образцами по бокам.
--   Синтаксис: `(<образец>) / (<образец>) = ...` — оператор можно задавать
--   инфиксно, ставя его между двумя аргументами-образцами.
--   Деление дробей = умножение на перевёрнутую: (a/b)/(c/d) = (a*d)/(b*c).
--   Зачем `reduce`: держим результат в несократимом виде.
  fromRational q = reduce (R (fromInteger (numerator q)) (fromInteger (denominator q)))
-- Метод `fromRational` обязателен для класса Fractional: он строит наш тип
--   из стандартной дроби `q` (тип Prelude `Ratio Integer`).
--   `numerator q` / `denominator q` — извлекают числитель и знаменатель (Integer).
--   `fromInteger ...` — сжимают Integer до Int для полей конструктора `R`.
--   Зачем: без `fromRational` экземпляр Fractional неполон; именно он позволяет
--   дробным литералам (например 0.5) превращаться в наш Rational.

-- -----------------------------------------------------------------------------
-- instance Eq Rational — равенство через сокращение
-- -----------------------------------------------------------------------------

instance Eq Rational where
-- Экземпляр класса `Eq` — типы, которые можно сравнивать на равенство `(==)`.
--   Зачем: вручную (без `deriving`), потому что 2/4 и 1/2 должны быть равны,
--   хотя их представления (поля Int) различаются.
  r1 == r2 = a == c && b == d
-- Реализация оператора `(==)`.
--   `&&` — логическое «И». `a == c` и `b == d` — сравнение целых полей.
--   Зачем: сравниваем не исходные дроби, а их сокращённые формы (см. ниже),
--   поэтому равны именно эквивалентные дроби, а не побитово одинаковые.
    where R a b = reduce r1
-- `where` с сопоставлением с образцом в определении: вычисляем `reduce r1`
--   и тут же распаковываем результат в `a` (числитель) и `b` (знаменатель).
--   Зачем: приводим первую дробь к канонической форме перед сравнением.
          R c d = reduce r2
-- То же самое для второй дроби: каноническая форма -> `c` и `d`.
--   Зачем: только после сокращения обеих дробей равенство полей означает
--   равенство значений.

-- -----------------------------------------------------------------------------
-- instance Ord Rational — порядок через кросс-умножение
-- -----------------------------------------------------------------------------

instance Ord Rational where
-- Экземпляр класса `Ord` — типы с отношением порядка (<, >, compare и т.д.).
--   Требует, чтобы тип уже был экземпляром `Eq` (что мы сделали выше).
--   Зачем: чтобы можно было сравнивать дроби по величине.
  compare r1 r2 = compare (a * d) (c * b)
-- Достаточно реализовать метод `compare` — остальные (<, <=, max ...)
--   класс выведет автоматически.
--   `compare (a*d) (c*b)` — кросс-умножение: a/b ? c/d равносильно a*d ? c*b,
--   ЕСЛИ знаменатели положительны.
--   Зачем кросс-умножение: сравнить дроби без перехода к Double (без потери
--   точности и без деления).
    where R a b = reduce r1
-- Сокращаем первую дробь; `reduce` гарантирует положительный знаменатель `b`.
          R c d = reduce r2
-- Сокращаем вторую дробь; знаменатель `d` тоже станет положительным.
--   Зачем нормализация знаков: при положительных b и d направление неравенства
--   при кросс-умножении не переворачивается, и сравнение корректно.

-- -----------------------------------------------------------------------------
-- instance Show Rational — красивый вывод (добавлен для удобства тестирования)
-- -----------------------------------------------------------------------------

instance Show Rational where
-- Экземпляр `Show` — превращение значения в строку (нужен, чтобы `print`
--   и GHCi могли отображать дробь).
--   Зачем вручную: по умолчанию вывелось бы «R 3 4»; мы хотим вид «3/4».
  show r = case reduce r of
-- Метод `show` возвращает строковое представление.
--   `case <выражение> of <образцы>` — разбор по образцам внутри выражения.
--   Здесь сначала сокращаем дробь, затем выбираем способ вывода.
--   Зачем `case`: позволяет по-разному показать целое число и настоящую дробь.
             R a 1 -> show a
-- Образец `R a 1` — совпадает, когда знаменатель буквально равен 1.
--   `1` здесь — образец-литерал (а не переменная), сравнивается на равенство.
--   Тогда выводим только числитель: целые числа без «/1».
--   Зачем: «5» читается лучше, чем «5/1».
             R a b -> show a ++ "/" ++ show b
-- Общий образец: любой знаменатель. `++` — конкатенация строк.
--   `show a` и `show b` — строковые представления чисел `Int`.
--   Зачем: формируем привычную запись «числитель/знаменатель».

-- -----------------------------------------------------------------------------
-- main — демонстрация всех функций и операций
-- -----------------------------------------------------------------------------

main :: IO ()
-- Сигнатура точки входа. `IO ()` — действие ввода-вывода, не возвращающее
--   полезного значения (`()` — пустой кортеж, «ничего»).
--   Зачем: любая программа на Haskell стартует с `main :: IO ()`.

main = do
-- `do`-нотация последовательно выполняет действия ввода-вывода.
--   Синтаксис: блок `do`, в котором каждая строка — действие или `let`-связка.
--   Зачем: удобно описывать серию шагов (печать, вычисления) по порядку.
  let z1 = R 3 4
-- `let <имя> = <значение>` внутри `do` вводит локальную чистую переменную.
--   `R 3 4` — создаём дробь 3/4 с помощью конструктора.
--   Зачем `let` (а не `<-`): значение чистое, без ввода-вывода.
  let z2 = R 1 (-2)
-- Ещё одна дробь: 1/(-2). Скобки вокруг `(-2)` обязательны.
--   Зачем скобки: без них `R 1 -2` воспринялось бы как вычитание `R 1` минус `2`.
  let z3 = R 6 8
-- Заведомо сократимая дробь 6/8 — для демонстрации `reduce`.

  putStrLn ("z1 = " ++ show z1)
-- `putStrLn` печатает строку и переводит строку.
--   `("z1 = " ++ show z1)` — склейка текста с представлением дроби.
--   Зачем: показать значение в человекочитаемом виде.
  putStrLn ("z2 = " ++ show z2)
-- Печать второй дроби.
  putStrLn ("reduce (6/8) = " ++ show (reduce z3))
-- Демонстрация `reduce`: 6/8 должно сократиться до 3/4.
  putStrLn ("z1 + z2 = " ++ show (z1 + z2))
-- Демонстрация оператора `(+)` из экземпляра Num (3/4 + (-1/2) = 1/4).
  putStrLn ("z1 * z2 = " ++ show (z1 * z2))
-- Демонстрация `(*)` из Num.
  putStrLn ("negate z1 = " ++ show (negate z1))
-- Демонстрация `negate` из Num.
  putStrLn ("z1 / z2 = " ++ show (z1 / z2))
-- Демонстрация `(/)` из экземпляра Fractional.
  putStrLn ("toDouble z1 = " ++ show (toDouble z1))
-- Демонстрация `toDouble`: печать 0.75 (тип Double).
  putStrLn ("z1 == reduce z3 ? " ++ show (z1 == reduce z3))
-- Демонстрация `(==)`: 3/4 == reduce(6/8) -> True.
  putStrLn ("z2 < z1 ? " ++ show (z2 < z1))
-- Демонстрация `(<)`, выведенного из `compare` в экземпляре Ord.
  putStrLn ("compare z1 z2 = " ++ show (compare z1 z2))
-- Демонстрация самого `compare`: вернёт GT, LT или EQ.
